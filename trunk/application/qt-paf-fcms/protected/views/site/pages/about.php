<h2>The Office of the Finance</h2>

<p>The Office of the Finance, Philippine Air Force was activated as a Special staff of the Commanding General, PAF on October 03, 1948. The office was responsible in monitoring all financial activities of the entire Command. Their mission is to provide financial management and services to the Philippine Air Force.</p>

<h4>PAFFC Functions</h4>
<ol>
<li>Disburse funds of the PAF strictly in accordance with laws and regulations and render reports required under existing regulations;</li>
<li>Provides financial services to personnel and units including those detailed outside PAF based on sites and other client agencies served by the PAF;</li>
<li>Provide the cash required by regular and special disbursing officer and personnel going on official mission or schooling/training;</li>
<li>Draws, prepares and signs checks, warrants for the CS, AFP under sec616 RAC, as amended, chargeable against the appropriation of the PAF;</li>
<li>Administers the current accounts of the PAF with Authorized Government Depository Bank;</li>
<li>Maintains centralized pay records of both military and civilian personnel;</li>
<li>Operates and maintains the PAF payroll system for military and civilian personnel;</li>
<li>Provides technical training and motivation to personnel charged with custody, handling and disbursements of funds both appropriated and non-appropriated.</li>
</ol>

